		a.CANTIDAD, a.VALOR, a.VALORCOPAGO, a.VALORITEM, a.VRNETO, FTR.ESTADO AS ESTADOFACTURA,CAPITADO=coalesce(KCNT.CAPITA,0),CIRUGIA=0,a.IDCIRUGIA,
