		JOIN dbo.HPRE  ON HADM.NOADMISION    = HPRE.NOADMISION and HPRE.TIPOPRESTACION = 0 AND (HPRE.CIRUGIA is null or HPRE.CIRUGIA<>'Si')
		JOIN dbo.HPRED ON HPRED.NOPRESTACION = HPRE.NOPRESTACION  /*AND HPRED.VALOR > 0 */ 		
		JOIN dbo.AFI   ON AFI.IDAFILIADO     = HADM.IDAFILIADO 
		JOIN dbo.SER   ON SER.IDSERVICIO     = HPRED.IDSERVICIO
		JOIN dbo.RENCP ON RENCP.IDCONCEPTORIPS = SER.CODIGORIPS 
	WHERE 
		(HADM.CLASENOPROC is null or HADM.CLASENOPROC<>'NP')	-- Excluye los No procesar
		AND NOT (RENCP.ARCHIVO in ('AH','AU') AND HADM.CLASEING<>'A') -- Excluye las que no son del Modulo Hospitalario para AH y AU
		-- AND ( HADM.CLASEING='A' or (HADM.CLASEING<>'A' and RENCP.ARCHIVO<>'AH' and RENCP.ARCHIVO<>'AU') ) -- Excluye las que no son del Modulo Hospitalario para AH y AU
