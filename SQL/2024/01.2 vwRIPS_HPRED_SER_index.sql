		round(CAST(abs(COALESCE(HPRED.VALOR,0)) AS DECIMAL(15,2)),0) AS VALOR, round(CAST(abs(COALESCE(HPRED.VALORCOPAGO,0)) AS DECIMAL(15,2)),0) AS VALORCOPAGO, 
