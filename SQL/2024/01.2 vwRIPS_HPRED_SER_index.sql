		SER.TIPOMED,LEFT(coalesce(SER.DESCSERVICIOCUPS,SER.DESCSERVICIO),60) AS DESCSERVICIO, LEFT(SER.NOM_GENERICO,60) AS NOMGENERICO,
		HADM.VIAINGRESO, HADM.DXEGRESO AS IDDX, 2 AS TIPODX,
